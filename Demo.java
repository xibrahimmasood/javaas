package ifelse;

public class Demo {

	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println("do while "+i);
			i+=1;
		} while(i<=5);
		
		int j = 1;
		while(j<=10) {
		System.out.println("while " +j);
		j++;
		}
		for(int k=1;k<=5;k++) {
			System.out.println("For Loop "+k);
		}
		
		System.out.println("Program ends");
		

	}

}
